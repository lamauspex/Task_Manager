
# Линтер для анализа качества кода
echo "Начинаем анализ кода..."

# Перемещаемся в корневую директорию проекта
cd $(dirname "$0")/.. || exit

# Анализируем весь проект с помощью flake8
flake8 .

# Дополнительно применим black для автоматического форматирования
black --check .

if [[ $? -eq 0 ]]; then
    echo "Анализ кода прошел успешно!"
else
    echo "Ошибка в анализе кода!"
    exit 1
fi